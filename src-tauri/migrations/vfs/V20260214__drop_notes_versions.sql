-- ============================================================================
-- V20260214: 移除笔记版本历史系统
-- ============================================================================
-- 
-- 笔记版本历史功能存在以下设计缺陷，决定移除：
-- 1. notes_versions 表无 __change_log 触发器，版本历史不参与跨设备同步
-- 2. VFS 路径无节流/清理机制，每次保存都创建快照导致数据库膨胀
-- 3. 功能实际使用价值有限
--
-- 此迁移会：
-- - 删除 notes_versions 表及其索引
-- - 不会删除 resources 表中的关联数据（由 GC 或自然淘汰处理）
-- ============================================================================

DROP TABLE IF EXISTS notes_versions;
